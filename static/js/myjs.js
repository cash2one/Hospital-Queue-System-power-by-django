$('#weibo').click(function(e){
	var $me = $(this), interval;
	
	e.preventDefault();
	
    $me.grumble({
		text: 'Whoaaa, this is a lot of text that i couldn\'t predict',
		angle: 20,
		distance: 2,
		showAfter: 0,
		hideAfter: false,
		hasHideButton: true,
		type: 'alt-', 
	});
});

